const uint8_t *font_get_glyph(uint32_t codepoint) {
    // ASCII range (32–126)
    if (codepoint >= 32 && codepoint <= 126) {
        return font_data[codepoint - 32];
    }

    // Block Elements (U+2580–U+2595)
    if (codepoint >= 0x2580 && codepoint <= 0x2595) {
        return block_elements[codepoint - 0x2580];
    }

    // Box Drawing (U+2500–U+253C, sparse)
    switch (codepoint) {
    case 0x2500: return box_2500; // ─
    case 0x2501: return box_2501; // ━
    case 0x2502: return box_2502; // │
    case 0x2503: return box_2503; // ┃
    case 0x250C: return box_250C; // ┌
    case 0x2510: return box_2510; // ┐
    case 0x2514: return box_2514; // └
    case 0x2518: return box_2518; // ┘
    case 0x251C: return box_251C; // ├
    case 0x2524: return box_2524; // ┤
    case 0x252C: return box_252C; // ┬
    case 0x2534: return box_2534; // ┴
    case 0x253C: return box_253C; // ┼
    default: break;
    }

    // Unknown codepoint – render a space (invisible) instead of a box.
    // This keeps alignment correct for unsupported Unicode characters.
    if (codepoint > 126) {
        return font_data[0]; // space glyph
    }

    return fallback_glyph;
}
